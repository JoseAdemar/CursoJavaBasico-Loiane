package com.junior.cursojava.aula17LoopFor;

public class LoopFor {
	public static void main(String[] args) {

		int contador = 11;

		for (int i = 1; i < contador; i++) {

			System.out.println("Valor de i: " + i);
		}
		
		
		for(int i = 0; i >= 0; i ++) {
			
			System.out.println("Loop infnito !!!");
		}
	}
}
